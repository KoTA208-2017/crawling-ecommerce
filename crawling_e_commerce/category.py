class Category():
    TOP = 'top'
    LONG = 'long'
    BOTTOM = 'bottom'

    def select_top(self):
        """Function to return category 'top' on an item"""
        return Category.TOP

    def select_long(self):
        """Function to return category 'long' on an item"""
        return Category.LONG
    
    def select_bottom(self):
        """Function to return category 'bottom' on an item"""
        return Category.BOTTOM
